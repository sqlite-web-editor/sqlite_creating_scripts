import sqlite3

# Подключение к базе данных (если файла нет, он будет создан)
conn = sqlite3.connect('ученики_оценки.db')
cursor = conn.cursor()

# Создание таблицы с полями, понятными для учителей
cursor.execute('''CREATE TABLE IF NOT EXISTS Оценки (
                    id INTEGER PRIMARY KEY,
                    имя_ученика TEXT,
                    предмет TEXT,
                    оценка INTEGER
                )''')

# Пример данных для заполнения таблицы
ученики = [
    ("Иванов", "Математика", 4),
    ("Петров", "Математика", 5),
    ("Сидорова", "Математика", 3),
    ("Иванов", "Литература", 5),
    ("Петров", "Литература", 4),
    ("Сидорова", "Литература", 4),
    ("Иванов", "Физика", 3),
    ("Петров", "Физика", 5),
    ("Сидорова", "Физика", 4)
]

# Заполнение таблицы данными об учениках и их оценках
for ученик in ученики:
    cursor.execute('''INSERT INTO Оценки (имя_ученика, предмет, оценка)
                      VALUES (?, ?, ?)''', ученик)

# Применение изменений и закрытие соединения
conn.commit()
conn.close()
